public class Arithmetic {
    public static void main(String[] args) {
        int a=11;
        int b =5;
        System.out.println("a+b = " +(a+b));
        System.out.println("a-b = " +(a-b));
        System.out.println("a*b = " +(a*b));
        System.out.println("a/b = " +(a/b));
        System.out.println("a%b = " +(a%b));
        
    }
}


